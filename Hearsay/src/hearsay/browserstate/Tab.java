package hearsay.browserstate;

public class Tab 
{
	private Integer tabId;
	
	private Object domTree;
	
	public Tab(Integer tabId, Object domTree)
	{
		this.tabId = tabId;
		this.domTree = domTree;
	}

	public Integer getTabId()
	{
		return tabId;
	}

	public void setTabId(Integer tabId)
	{
		this.tabId = tabId;
	}

	public Object getDomTree()
	{
		return domTree;
	}

	public void setDomTree(Object domTree)
	{
		this.domTree = domTree;
	}
	
}
